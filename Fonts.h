static inline
unsigned short color_to_abgr4444 (unsigned char rgba [4])
{
        unsigned short __attribute__((aligned(16))) c;
        c  = rgba[0] >> 4;
        c |= rgba[1] & 0x00f0; 
        c |= (rgba[2] << 4) & 0x0f00;
        c |= (rgba[3] << 8) & 0xf000;
        return c;
}

