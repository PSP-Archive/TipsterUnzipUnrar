#define SWAP(a, b) {(b) = (a)^(b); (a) ^= (b); (b) ^= (a);}

void DrawLineRGB(int sx, int sy, int dx, int dy, int r, int g, int b,  unsigned short *pixlist, int bufwid)
{
	//This Implementation of Brensenham's Line Drawing
	//Algorithm has been copied from ReKleSS's Software
	//3D renderer for PSP
	int x, y;
	if (sx == dx && sy == dy) {
		SetPixel2D(sx, sy,255, r ,g ,b,pixlist,bufwid);
		return;
	}
	if (sx == dx) {
		if (sy > dy) SWAP(sy, dy);
		for (y = sy; y < dy;y++)
		SetPixel2D(sx, y,255, r ,g ,b,pixlist,bufwid);
		return;
	} else if (sy == dy) {
		if (sx > dx) SWAP(sx, dx);
		for (x = sx;x < dx;x++)
			SetPixel2D(x, sy,255, r ,g ,b,pixlist,bufwid);
		return;
	}

	
	//bresenham's (complete) algo
	int steep = abs(dy - sy) > abs(dx - sx);
	if (steep) {
		SWAP(sx, sy);
		SWAP(dx, dy);
	}

	int deltx = abs(dx - sx);
	int delty = abs(dy - sy);
	int error = 0;

	int delterr = delty;
	x = sx;
	y = sy;

	int xs = (sx < dx) ? 1 : -1;
	int ys = (sy < dy) ? 1 : -1;

	if (steep) SetPixel2D(y, x,255, r ,g ,b,pixlist,bufwid);
	else  SetPixel2D(x, y,255, r ,g ,b,pixlist,bufwid);

	while (x != dx) {
		x += xs;
		error += delterr;
		if ((error << 1) >= deltx) {
			y += ys;
			error -= deltx;
		}
		if (steep) SetPixel2D(y, x,255, r ,g ,b,pixlist,bufwid);
		else SetPixel2D(x, y,255, r ,g ,b,pixlist,bufwid);
	}
}

