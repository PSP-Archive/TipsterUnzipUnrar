void printDecimal(int x, int y, int value, int r, int g, int b,  unsigned short *pixlist, int bufwid)
{
	int i;
	int digits[6];
	for (i = 5; i >= 0; i--) {
		digits[i] = value%10;
		value /= 10;
	}
	i = 0;
	while (i < 5 && digits[i] == 0) digits[i++] = -1;
	for (i = 0; i < 6; i++) {
		if (digits[i] >= 0) {
			print7Segment(x, y, digits[i], r, g, b,pixlist,bufwid);
			x += 9;
		}
	}
}

